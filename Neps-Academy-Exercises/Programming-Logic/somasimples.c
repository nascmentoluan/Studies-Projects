#include <stdio.h>

int main() {
    int A, B; //Declaração das variáveis A e B.

    scanf("%d", &A); //Função para leitura do valor que será atribuído à variável A

    scanf("%d", &B); //Função para leitura do valor que será atribuído à variável B
    
    printf("%d", A+B); //Função para exibir resultado da soma dos valores de A e B

    return 0;
}